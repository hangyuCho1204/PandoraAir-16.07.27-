package mypkg.model;

public class Airplane {

	private String airplane;
	private String category;
	private int seat_qty;
	private int fare;
	private int saving_point;
	
	public String getAirplane() {
		return airplane;
	}
	public void setAirplane(String airplane) {
		this.airplane = airplane;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getSeat_qty() {
		return seat_qty;
	}
	public void setSeat_qty(int seat_qty) {
		this.seat_qty = seat_qty;
	}
	public int getFare() {
		return fare;
	}
	public void setFare(int fare) {
		this.fare = fare;
	}
	public int getSaving_point() {
		return saving_point;
	}
	public void setSaving_point(int saving_point) {
		this.saving_point = saving_point;
	}
}
